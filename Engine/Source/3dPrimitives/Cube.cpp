Cube::~Cube()
{
	std::cout << "Delete cube" << std::endl;
    delete mVertexBuffer;
}

void Cube::OnUpdate(float deltaTime)
{
    Entity3D::OnUpdate(deltaTime);
}

void Cube::OnDraw()
{
    mMaterial->SetActive();

    mMaterial->GetShader()->SetMat4("model", mModelMatrix);

    mVertexBuffer->Draw();
}

void Cube::OnDraw(Shader* s)
{
    mMaterial->SetActive();
    s->SetActive();

    s->SetMat4("model", mModelMatrix);

    mVertexBuffer->Draw();
}
